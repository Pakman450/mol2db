class atoms:
    def __init__(self):
        self.atom_num = []
        self.atom_name = []
        self.x = []
        self.y = []
        self.z = []
        self.atom_type = []
        self.subst_id = []
        self.subst_name = []
        self.charge = []

    def clear(self):
        self.atom_num = []
        self.atom_name = []
        self.x = []
        self.y = []
        self.z = []
        self.atom_type = []
        self.subst_id = []
        self.subst_name = []
        self.charge = []
